# Basic Arithmetic Operations

first_number = 10
second_number = 20

addition_result = first_number + second_number      # Addition
subtraction_result = first_number - second_number   # Subtraction
multiplication_result = first_number * second_number # Multiplication
division_result = first_number / second_number      # Division

print("Addition :", addition_result)
print("Subtraction :", subtraction_result)
print("Multiplication :", multiplication_result)
print("Division :", division_result)


# Rectangle Area and Perimeter

rectangle_length = 10
rectangle_breadth = 5

rectangle_area = rectangle_length * rectangle_breadth
rectangle_perimeter = 2 * (rectangle_length + rectangle_breadth)

print("Area of Rectangle :", rectangle_area)
print("Perimeter of Rectangle :", rectangle_perimeter)


# Circle Area and Circumference

circle_radius = 20
pi_value = 3.14

circle_area = (pi_value * circle_radius) ** 2
circle_circumference = 2 * pi_value * circle_radius

print("Circle Area :", circle_area)
print("Circumference :", circle_circumference)


# Simple Interest Calculation

principal_amount = 100
interest_rate = 2
time_period = 2

simple_interest = (principal_amount * interest_rate * time_period) / 100

print("Simple Interest is :", simple_interest)